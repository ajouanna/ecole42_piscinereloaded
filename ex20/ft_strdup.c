#include <stdlib.h>

char		*ft_strdup(char *src)
{
	int		len;
	int		i;
	char	*res;

	len = 0;
	while (src[len])
		len++;
	res = malloc(sizeof(char) * (len + 1));
	if (res == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		res[i] = src[i];
		i++;
	}
	res[i] = 0;
	return (res);
}
